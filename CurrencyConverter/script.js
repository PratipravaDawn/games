const BASE_URL =
  "https://latest.currency-api.pages.dev/v1/currencies/eur.json"


let dropdowns = document.querySelectorAll('.dropdown select');
let convertBtn = document.querySelector('button');
let fromCurr = document.querySelector('select#from');
let toCurr = document.querySelector('select#to');
let msg = document.querySelector('.message');

for (let select of dropdowns) {
    for (Currcodes in countryList){
        let newOption = document.createElement('option');
        newOption.innerText = Currcodes;
        newOption.value = Currcodes;
        if (newOption.value === 'USD' && select.id === 'from') {
            newOption.selected = "selected";}
        if (newOption.value === 'INR' && select.id === 'to') {
        newOption.selected = "selected";}
        select.append(newOption);
    }
    select.addEventListener('change', (evt) =>{
        updateFlag(evt.target);
    });
}

const updateFlag = (element) => {
    Currcodes = element.value;
    countryCode = countryList[Currcodes];
    let image = document.querySelector(`.${element.id} img`);
    image.src = `https://flagsapi.com/${countryCode}/flat/64.png`;
}

const updateCurency = async () => {
    let amt = document.querySelector('input#amount');
    let amount = amt.value;
    if (amount === '' || amount <= 0) {
        amt.value = 1;
        amount = 1;
    }

    let res = await fetch(BASE_URL);
    let data = await res.json();
    let euroMid = data['eur'];

    let fromCurrvalue = euroMid[(fromCurr.value).toLowerCase()];
    let toCurrvalue = euroMid[(toCurr.value).toLowerCase()];


    let rate = (1 / fromCurrvalue) * toCurrvalue;
    let finalAmount = (amount * rate).toFixed(2);

    msg.innerText =`${amount} ${fromCurr.value} = ${finalAmount} ${toCurr.value}`;
};

convertBtn.addEventListener('click', (evt) => {
    evt.preventDefault();
    updateCurency();
});

window.addEventListener('load', () => {
    updateCurency();
});
